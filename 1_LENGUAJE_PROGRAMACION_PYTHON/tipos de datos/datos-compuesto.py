#Listas. Se pueden modificar
list=["Vcitor","Cruz", 40]
print(list[1])

#Tuplas. No se puede modificar
tuple=("Vcitor","Cruz", 40)
print(tuple[0])

#Set. Conjunto
#No tienen orden
#No se modifican cada elemento
#No almacena datos duplicados
#No se acceden por indice a los elementos
set_example={"Vcitor","Cruz", 40}
print(set_example)

